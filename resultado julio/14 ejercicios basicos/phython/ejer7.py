pi = 3.1416
radio = float(input("ingrese el radio de su circulo"))
peri = 2*pi*radio
area = pi*(radio*radio)
vol = 4*pi*(radio*radio*radio)/3
print (f"area = {pi} * {radio} elevado a la 2 = {area}")
print (f"perimetro = 2 * {pi} * {radio} = {peri}")
print (f"volumen = 4 * {pi} * {radio} elevado a la 3 / 3 = {vol}")